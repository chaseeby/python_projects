# Create List of 5 places
locations = ['Hawaii', 'Texas', 'New York', 'California', 'Kansas']
print(locations)
# Sort the list
print(sorted(locations))
print(locations)
# Reverse the List
locations.reverse()
print(locations)
# reverse list back into original order
locations.reverse()
print(locations)

# use sort()
locations.sort()
print(locations)

locations.sort(reverse=True)
print(locations)
